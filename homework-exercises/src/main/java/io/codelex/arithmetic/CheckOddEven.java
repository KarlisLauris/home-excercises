package io.codelex.arithmetic;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 6;
        if(number % 2 == 0) {
            System.out.println("Number is even");
        } else {
            System.out.println("Number is odd");
        }
        System.out.println("Bye!");
    }
}
